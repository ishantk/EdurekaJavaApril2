package co.edureka.model;

public class Student {
	
	public int roll;
	public String name;
	public int age;
	public String email;
	public String address;

	public String toString() {
		return "Student [roll=" + roll + ", name=" + name + ", age=" + age + ", email=" + email + ", address=" + address
				+ "]";
	}
	
	
	
}
